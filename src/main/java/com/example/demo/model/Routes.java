package com.example.demo.model;
public class Routes {
    public int id;
    public String route_kod;
    public int num;
    public String route_point;
    public String come;
    public String parking;
    public int weight;

    public void setId(int id) {
        this.id = id;
    }

    public void setRoute_kod(String route_kod) {
        this.route_kod = route_kod;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public void setRoute_point(String route_point) {
        this.route_point = route_point;
    }

    public void setCome(String come) {
        this.come = come;
    }

    public void setParking(String parking) {
        this.parking = parking;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getId() {
        return id;
    }

    public String getRoute_kod() {
        return route_kod;
    }

    public int getNum() {
        return num;
    }

    public String getRoute_point() {
        return route_point;
    }

    public String getCome() {
        return come;
    }
    public String getParking() {
        return parking;
    }

    public int getWeight() {
        return weight;
    }

}

